
''' Program to take number from User and then print Square root , logarithum and Sin
using math module'''

#import library
import math

# User input
number = int(input("Enter a number : "))

# print required results using maths library
print(f"Square root : {math.sqrt(number)}")
print(f"Logarithm : {math.log(number)}")
print(f"Sin : {math.sin(number)}")
